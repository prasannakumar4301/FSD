//string
//string is sequence of characters, it is immutable
var empName = "Prasanna Kumar"
console.log(empName, typeof(empName)) // Sai Kiran string

//number
//empSalary, empId
var empSalary = 20000.00 
console.log(empSalary, typeof(empSalary)) // 20000 number

var empId = 25689
console.log(empId, typeof(empId)) // 25689 number

//boolean
var empLeave = false
console.log(empLeave, typeof(empLeave)) // false boolean

//array 
var l1 = [1, 2, 3, 4, 5]
console.log(l1, typeof(l1)) //[ 1, 2, 3, 4, 5 ] object

//object
var d1 = {1:10, 2:20, 3 :30, 4:40, 5:50}
console.log(d1, typeof(d1)) // { '1': 10, '2': 20, '3': 30, '4': 40, '5': 50 } object

//undefined
var hireEmployee = undefined
console.log(hireEmployee, typeof(hireEmployee)) // undefined undefined

//null
var userName = null
console.log(userName, typeof(userName)) // null object
